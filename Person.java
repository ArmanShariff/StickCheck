
public class Person{

    String firstName;
    String lastName;
    String position;
    String country;
    int age;
    int playerNumber;
    int contract;
    
    public Person(String pFirstName, String pLastName, String pPosition, String pCountry, int pAge, int pPlayerNumber, int pContract) {
        
        firstName = pFirstName;
        lastName = pLastName;
        position = pPosition;
        country = pCountry;
        age = pAge;
        playerNumber = pPlayerNumber;
        contract = pContract;
    }

}